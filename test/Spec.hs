{-|
Construct an IntCode machine from given comma
separated int values

Run the machine and return the final output (element at index 0)
-}
runTest :: String -> IO Int
runTest inp = do
    -- Convert the string into a list of ints
    let inpl = map read . splitStrAt "," $ inp
    -- Set start index to 0 and len to length of input list
        (start, len) = (0, length inpl)
    -- Construct the mutable IntCode array
    inpArr <- constructArray inpl
    -- Mutate the array by running the intcode instructions
    runMachine inpArr len start
    -- Return the resulting, mutated intcode array's 0th element
    getOutput inpArr

